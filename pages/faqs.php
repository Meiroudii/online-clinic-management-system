		<bold><strong><font size="5">Frequently Asked Questions for Patients</bold></strong></font>
		<br>
		<br><bold><strong><font size="3.5">
		Q.  What are your regular office hours?</strong></bold></font>
		 <br>
		 <br>
		 
		A.  Our regular clinic hours are 10:30AM - 6:30 PM, Monday through Saturday, except Sunday.As Sunday remains as a holiday. Our phone service hours are 10 AM - 7 PM, Monday through Saturday, except Sunday.
		 <br>
		 <br>
		 <bold><strong><font size="3.5">
		Q. What is your appointment policy?</strong></bold></font>
		<br>
		 <br>
		 
		A.  Our physicians must work by appointment. Unfortunately, occasionally patient appointments do require longer time frames than scheduled. When this happens, your patience is appreciated. A sincere attempt is made to adhere to the clinic schedule as much as possible. The registration staff will advise you if the physician is significantly behind schedule. We ask that whenever possible, a return appointment for regular checkups be made before leaving the clinic.
		 
		<br>
		 <br> 
		 <bold><strong><font size="3.5">
		Q.  Do I need to bring anything special to my first visit?</strong></bold></font>
		<br>
		 <br>
		 
		A.  We want to ensure you are seen quickly once you arrive and that your time with the physician is used effectively. It will be helpful if you bring to all your visits your current insurance card, photo ID, and any medications you are taking. Also, any forms or test results that you have been provided, such as x-rays. If your insurance has a co-pay for an office visit, you will be responsible to pay at the time of your visit. A helpful form may be found under the 'patient forms' section in the patient care section called 'Speakup - Tips for Your Doctor's Visit.'
		<br>
		 <br>
		 <bold><strong><font size="3.5">
		Q.  What can I expect at the first appointment?</strong></bold></font>
		 <br>
		 <br>
		A.  Our registration staff will ask you to fill out a health history form. These questions are the initial part of the examination and provide us with essential information about you and your medical history. Your address, e-mail and phone number are very important in case we need to get in touch with you. Information concerning your occupation, age, etc., is likewise important because it may have a bearing on your current health status. Additionally, this information is required by insurance networks to insure payment reimbursement. We also ask about language, hearing or vision barriers to allow us to appropriately communicate with you. All information given to us is confidential.
		<br>

		We ask you to assist us in selecting a Continuity of Care provider for you. This provider will be part of a team that will care for you and includes a nursing staff, residents, and physicians.
		<br>
		 
		When it is time to be seen by the physician, the nurse will escort you into an exam room and make you comfortable and complete vital signs. Give some thought to your medical history beforehand, for example your height, weight, and allergies. It is helpful if you can remember to bring in a list of all current medications you are taking or the medication bottles themselves, including over-the-counter and herbal supplements. The physician will complete an interview and  physical exam to complete a correct problem list, medication list and plan of care. After the visit is complete, the registration/scheduler will make a return appointment, if necessary and provide you with a copy of your visit summary which will include your treatment plan and updated medication list.
		 <br>
		 <br>
		 <bold><strong><font size="3.5">
		Q.  How do I choose a Continuity of Care Provider?</strong></bold></font>
		<br>
		 <br> 
		A.  As a Primary Care Medical Home, we ask you to work with our clinical staff to select a Continuity of Care Provider. At your first visit you will have input into your Continuity of Care Provider, and will be able to choose a team of clinicians that will establish a health care relationship with you to provide the best preventative health care possible. As part of the Patient Centered Medical Home initiative, all patients are assigned to a Continuity of Care Provider who functions as part of a team of physicians to allow for patient centered, continuous care for you as the patient.
		<br>
		 <br> 
		 <bold><strong><font size="3.5">
		Q.  How do I get a refill on my prescription?</strong></bold></font>
		 <br>
		 <br>
		A.  We ask that prescription refills be completed during regular office visits whenever possible. Calls for prescription refills may be made 24 hours a day through our phone tree. Physicians must have the benefit of reviewing your medical record before prescribing any additional medications. Calculate in advance when you may need a refill on medicines that were prescribed by us, as we require at least 72 hours notice. Please allow at least 7 days to refill narcotics. When calling the clinic, choose #4 on the phone tree, and follow the prompts. Please have the medication name and dose, pharmacy name and number available, and leave your name and date of birth and attending physician, so we may expedite your request. Certain prescriptions must be picked up and signed for during regular office hours.
		 <br>
		 <br>
		 <bold><strong><font size="3.5">
		Q.  What should I do if  I can't bring my minor child to his or her appointment?</strong></bold></font>
		 
		A.  We ask that you  send a detailed note with the individual bringing the child to the appointment giving permission for medical care, including immunizations giving permission to treat your child. A form can also be found under the 'patient forms' section in the patient care section called 'Permission to Treat Minor child Form'.
		 <br>
		 <br>
		 <bold><strong><font size="3.5">
		Q.  What should I do if I'm ill?</strong></bold></font>
		 <br>
		 <br>
		A.  Our physicians are available 24 hours a day. If you are feeling ill, we ask that you call our office during regular office hours (10 AM - 7 PM Monday through Saturday, except on Sunday) and select #3 on the phone tree, and you will be connected to a phone triage RN who will instruct you on your options of care. That may be an urgent office visit or instructions to go immediately to the nearest Hospital Emergency Room.
		<br>

		WMed is designed to help meet your unexpected medical needs even when your continuity of care provider is not available. If you have a non-life-threatening illness that needs immediate attention, another provider from your care team will see you for a same-day appointment.
		<br>
		 
		We ask you to always contact your Continuity of Care provider when possible before going to the Emergency Room.
		<br>
		 
		If you or someone under your care is experiencing a life-threatening emergency, call 911. Please obtain copies of all tests taken and contact your physician as soon as possible to determine the best course of action.
		 <br>
		 <br>
		<bold><strong><font size="3.5"> 
		Q.  What should I do if I miss my appointment?</strong></bold></font>
		 
		A.  We ask that you call our office during regular office hours (10 AM - 7 PM Monday through Saturday, except on Sunday) and explain that you have missed your appointment and discuss rescheduling the appointment. Please keep in mind that this will appear as a No Show appointment unless notification occurs within 2 hours prior to your appointment.
		 <br>
		 <br>
		 <bold><strong><font size="3.5">
		Q.  What should I do if the office is closed?</strong></bold></font>
		 <br>
		 <br>
		A.  We provide our patients with 24 hour / 7 day coverage. There is always a physician on-call who can be reached through our main clinic numbers. If you feel your condition is truly a critical emergency, please call 911. One of our physicians is available for emergencies at all times. After office hours and on weekends, our telephone is answered by our answering service, which in turn relays the message promptly to the physician on call for emergencies.
		<br>
		 
		This may not be your usual physician, so be prepared to answer some health history questions for the on-call physician if needed. We suggest that you call us before going to one of the hospital emergency rooms, thereby minimizing patient costs, inconvenience, and lost time. It is important that you keep your line free so that the physician can reach you easily. If you have not been contacted within a 20 minute time frame, please feel free to call back.
		 
		 
		 <br>
		 <br>
		 <bold><strong><font size="3.5">
		Q. What if my family is in the clinic and I'm coming from out of town?</strong></bold></font>
		 <br>
		 <br>
		A.  The Hospital Hospitality of One Care Medical Clinic is a non-profit organization that provides rooms for families of patients from out of town when they have a family member who may not have the financial resources to remain in town during their family member's stay. For more information please feel free to cal us on 033-2442268
		 <br>
		 <br>
		 <bold><strong><font size="3.5">
		Q.  Why do I need a referral from my primary care physician to see a specialist?</strong></bold></font>
		 <br>
		 <br>
		A.  If you are insured by an HMO or in some cases a PPO, your health plan will probably require a referral for specialist care. Your primary care physician is your central contact for all medical concerns as this allows for continuity of care for members and allows a central location for medical records. Referrals are typically processed within 14 days, except in urgent situations, and you will be notified by mail when the referral has been processed.
		 
