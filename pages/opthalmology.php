<p><br>Opthalmology
	<br></br>

The Ophthalmology department has the expertise to treat all kind of eye related problems ranging from routine eye examination to other conditions of the eye and surrounding structures.<br></br>
Our team of ophthalmologists is fully equipped with state of the art diagnostic and therapeutic equipments for treating glaucoma, cataracts, corneal and external diseases, aesthetics, paediatric ophthalmic diseases and disorders.<br></br>
The ophthalmologists at our center are committed to providing comprehensive, world-class care. Using the latest, state-of-the art equipment and procedures, our board certified specialists treat a wide range of disease, both medically and surgically, in the pediatric and adult population. We are committed to excellence and look forward to serving you. 

Optimal vision through specialized services: <br></br>
<strong></strong>Vision correction
-Eye-test and lens prescription.
-Photorefractive keratectomy (PRK) surgical procedures. See Laser Vision Center for more on PRK at our center.

<strong></strong>Corneal and external disease<br></br>
-Corneal infections, dystrophies, degenerations, and corneal transplantation 

<strong></strong>Cataracts<br></br>
Cataract surgery - state-of-the-art surgical techniques, including phacoemulsification and small incision (no-stitch) surgery. 
 

<strong></strong>Glaucoma and anterior segment<br></br>
Treatment of all forms of glaucoma (congenital, primary open angle, and angle closure glaucoma). Glaucoma surgery may include - laser techniques (YAG, diode, and argon lasers), anti-metabolite assisted surgery, glaucoma shunts, and cyclophotocoagulation. 



