	Paediatrics<br></br>

	Welcome to the Pediatric Hematology/Oncology Service. As an integral component we provide initial consultation and on-going comprehensive care, both inpatient and outpatient, for infants, children, adolescents, and young adults with cancer and blood disorders.<br></br>.
	Our team of specialized physicians, nurses, and clinical social workers are dedicated to provide the best care for patients and support their families. From the time of diagnosis we provide comprehensive, family centered care.<br></br>
	As a member of the Children's Oncology Group, we offer the opportunity for participation in cutting edge international clinical trials for our cancer patients.<br></br>
	Outpatient chemotherapy and transfusion services are given in our brand new Pediatric Hematology/Oncology Clinic's Pediatric Infusion Center and procedures are performed in the specialized Pediatric Sedation Unit. <br></br>

Comprehensive multidisciplinary clinics are available for patients with sickle cell disease, hemophilia, and for cancer survivors. In a single day patients are seen by all the members of their team of care providers to include several pediatric subspecialists, child psychology, audiology, ophthalmology, and social work. 

